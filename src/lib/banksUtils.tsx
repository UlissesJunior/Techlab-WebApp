export function getBackgroundByLogoName(bank: BankName, metallic: boolean = true): string {
    if (!metallic) return "bg-white dark:bg-[#141414] dark:border-border dark:border dark:text-white";

    const bankStyles: Record<string, string> = {
        nubank: "bg-gradient-to-br from-[#820ad1] via-[#a030e0] to-[#6a00cc] shadow-purple-500/20",
        itau: "bg-gradient-to-br from-[#ec7000] via-[#ff8c00] to-[#d45a00] shadow-orange-500/20",
        bradesco: "bg-gradient-to-br from-[#cc092f] via-[#e61b3c] to-[#b3001e] shadow-red-500/20",
        santander: "bg-gradient-to-br from-[#ec0000] via-[#ff0000] to-[#c50000] shadow-red-500/20",
    };

    return bankStyles[bank.toLowerCase()] || "bg-gradient-to-br from-gray-700 to-gray-900";
}

export function getBankColor(bank: BankName): string {
    const colors: Record<string, string> = {
        nubank: '#820ad1',
        itau: '#ec7000',
        bradesco: '#cc092f',
        santander: '#ec0000',
    };

    return colors[bank.toLowerCase()] || '#6b7280';
}